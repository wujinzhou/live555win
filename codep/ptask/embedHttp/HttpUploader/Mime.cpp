string Mime::ext2mime(const string &ext){
	if(c_mmapMime.empty()){
		mimeinit();
	}
	map<string,string>::iterator it = c_mmapMime.find(ext);
	if(it==c_mmapMime.end()){
		return "";
	}
	return (*it).second;
}
